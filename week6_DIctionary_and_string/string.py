# ==================================== STRING =============================================
# Python akan memulai perhitungan dari nol 
pulau = "kalimantan"
print("\nPulau [2]")
print(pulau[2])
# Saat menggunakan -(minus) maka proses perhitungan akan dimulai dari -1
print("pulau [-2]: ")
print(pulau[-2])
print("//////////")
print("\nPrint liman [2:7]")
print(pulau[2:7])
print("Print limantan [2:]")
print(pulau[2:])
print("Print kali [:4]")
print(pulau[:4])
print("Print kalimantan [:]")
print(pulau[:])